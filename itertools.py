import itertools as it
c=it.combinations([1,2,3,4],2)
Lc=list(c)
print(len(Lc))
print(Lc)
Lp=list(it.permutations([1,2,3,4],2))
print(len(Lp))
print(Lp)
Lm=list(it.product([1,2,3,4]))
print(Lm)
print(list(it.accumulate([1,2,3,4,5])))
ch=it.chain([1,2,3,4,5])
print(ch.__next__())
print(ch.__next__())
print(ch.__next__())
cnt=it.count(0,2)
print(cnt.__next__())
print(cnt.__next__())
print(cnt.__next__())
print(cnt.__next__())
